/**
 ******************************************************************************
 * @brief   打印缓存
 * @param[in]  *pformat : 提示字符
 * @param[in]  *pbuffer : 缓存首地址
 * @param[in]  len      : 长度
 *
 * @return  None
 ******************************************************************************
 */
extern void
printbuffer(const char_t *pformat,
            const uint8_t *pbuffer,
            int32_t len)
{
    (void)printf(pformat);
    for (int32_t i = 0; i < len; i++)
    {
        (void)printf("%02X ", *(pbuffer + i));
    }
    (void)printf("\r\n");
}

